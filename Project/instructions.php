    <p>Who Murdered my Grade is a game for two to six players.</p>
    <h2>Set Up</h2>
    <p>To begin, Select the number of players for the game and then click submit.
        The next page will present the cards for the first player. When displayed,
        the page will not actually show any cards, but will instead give the option to print the
        players card and their hidden codeword set. The top cards are the ones that have been dealt to this player.
        The other cards are the secret cards. Each player will print their cards and then click next</p>
    <h2>Game Play</h2>
    <p>t is Prof. Owen's turn. Two dice has been rolled and are showing a 7. The Prof. Owen playing piece is in the
        board location labeled "Start Prof. Owen." Prof has two options at that point:
        The piece can be moved exactly 7 squares.
        Or the Prof. can move into a room if it is less than or equal to the seven squares</p>
    <p>Once a player enters a room, they have three choices:

        Pass, ending the turn.
        Make a suggestion.
        Make an accusation.
        Making a suggestion and an accusation are the same process, just different outcomes.

        The suspect is moved into the room. In this case Prof. McCullen's piece is moved into the Engineering Building.
        It does not matter if someone is playing Prof. McCullen or not. The piece is moved.
        Then the player must choose a weapon from the six possible options.</p>
    <h2>Winning</h2>
    <p>The game is one when a player successfully makes an accusation.</p>
